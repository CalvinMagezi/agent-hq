// ─── Morning brief helpers ───────────────────────────────────

fn gather_morning_context(vault_path: &Path) -> String {
    let mut parts = Vec::new();

    // Pending jobs
    let pending = vault_path
        .join("_jobs")
        .join("pending")
        .read_dir()
        .map(|e| e.count())
        .unwrap_or(0);
    parts.push(format!("- {pending} pending jobs in the queue"));

    // Yesterday's completed
    let yesterday = (chrono::Utc::now() - chrono::Duration::days(1))
        .format("%Y-%m-%d")
        .to_string();
    let completed = vault_path.join("_jobs").join("completed");
    if completed.exists() {
        let count = std::fs::read_dir(&completed)
            .map(|entries| {
                entries
                    .flatten()
                    .filter(|e| {
                        e.metadata()
                            .ok()
                            .and_then(|m| m.modified().ok())
                            .map(|t| {
                                let dt: chrono::DateTime<chrono::Utc> = t.into();
                                dt.format("%Y-%m-%d").to_string() == yesterday
                            })
                            .unwrap_or(false)
                    })
                    .count()
            })
            .unwrap_or(0);
        parts.push(format!("- {count} jobs completed yesterday"));
    }

    // News pulse headlines (first 5 items)
    let news = read_news_summary(vault_path);
    if !news.is_empty() {
        parts.push(format!("- Recent news:\n{news}"));
    }

    parts.join("\n")
}

fn read_news_summary(vault_path: &Path) -> String {
    let news_path = vault_path.join("_system").join("NEWS-PULSE.md");
    if let Ok(content) = std::fs::read_to_string(&news_path) {
        // Extract first 10 bullet points
        content
            .lines()
            .filter(|l| l.starts_with("- ") && !l.contains("no items parsed"))
            .take(10)
            .collect::<Vec<_>>()
            .join("\n")
    } else {
        "*No news pulse available.*".to_string()
    }
}

async fn get_calendar_agenda() -> String {
    // Try gws CLI for calendar
    match tokio::process::Command::new("gws")
        .args(["calendar", "+agenda"])
        .output()
        .await
    {
        Ok(output) if output.status.success() => {
            let text = String::from_utf8_lossy(&output.stdout).trim().to_string();
            if text.is_empty() {
                "*No calendar events today.*".to_string()
            } else {
                // Truncate to reasonable length
                text.chars().take(1000).collect()
            }
        }
        _ => "*Calendar not available (gws CLI not found or failed).*".to_string(),
    }
}

async fn generate_via_claude(prompt: &str) -> Result<String> {
    let output = tokio::process::Command::new("claude")
        .args(["-p", prompt, "--output-format", "text"])
        .stdout(std::process::Stdio::piped())
        .stderr(std::process::Stdio::piped())
        .stdin(std::process::Stdio::null())
        .output()
        .await?;

    if output.status.success() {
        Ok(String::from_utf8_lossy(&output.stdout).trim().to_string())
    } else {
        let stderr = String::from_utf8_lossy(&output.stderr);
        anyhow::bail!("claude exited with {}: {}", output.status, stderr.chars().take(200).collect::<String>())
    }
}

async fn generate_tts(text: &str, output_path: &str) -> Result<bool> {
    // Try Kokoro via python3.13 directly (matches SpeakTool logic)
    let home = dirs::home_dir().unwrap_or_default();
    let model_dir = home.join(".agent-hq").join("voice");
    let model_path = model_dir.join("kokoro-v1.0.int8.onnx");
    let voices_path = model_dir.join("voices-v1.0.bin");

    if model_path.exists() && voices_path.exists() {
        let script = r#"
import sys, os
import kokoro_onnx
import soundfile as sf
text    = sys.argv[1]
voice   = sys.argv[2] if len(sys.argv) > 2 else "af_heart"
speed   = float(sys.argv[3]) if len(sys.argv) > 3 else 1.0
out     = sys.argv[4] if len(sys.argv) > 4 else "/tmp/hq-speech.wav"
model   = sys.argv[5]
voices  = sys.argv[6]
kokoro = kokoro_onnx.Kokoro(model, voices)
samples, rate = kokoro.create(text, voice=voice, speed=speed, lang="en-us")
sf.write(out, samples, rate)
print(out)
"#;
        let script_file = std::env::temp_dir().join("hq-kokoro-brief.py");
        tokio::fs::write(&script_file, script).await?;

        // Strip [S1]/[S2] tags for cleaner TTS
        let clean_text: String = text
            .lines()
            .map(|l| {
                l.trim_start_matches("[S1]: ")
                    .trim_start_matches("[S2]: ")
            })
            .collect::<Vec<_>>()
            .join(" ");

        let output = tokio::process::Command::new("python3.13")
            .args([
                script_file.to_str().unwrap_or_default(),
                &clean_text,
                "af_heart",
                "1.0",
                output_path,
                model_path.to_str().unwrap_or_default(),
                voices_path.to_str().unwrap_or_default(),
            ])
            .output()
            .await?;

        return Ok(output.status.success());
    }

    // Fallback: macOS `say`
    for voice in &["Ava", "Zoe", "Samantha"] {
        if let Ok(o) = tokio::process::Command::new("say")
            .args(["-v", voice, "-o", output_path, text])
            .output()
            .await
        {
            if o.status.success() {
                return Ok(true);
            }
        }
    }

    Ok(false)
}

fn gather_notebooklm_sources(vault_path: &Path) -> Vec<String> {
    let mut sources = vec!["Profile & Calendar".to_string()];

    // AI Model Intelligence
    if vault_path
        .join("_system")
        .join("MODEL-INTELLIGENCE.md")
        .exists()
    {
        sources.push("AI Model Intelligence".to_string());
    }

    // News Pulse
    if vault_path.join("_system").join("NEWS-PULSE.md").exists() {
        sources.push("News Pulse Headlines".to_string());
    }

    // Pinned notes from vault
    let notebooks_dir = vault_path.join("Notebooks").join("Projects");
    if notebooks_dir.exists() {
        if let Ok(entries) = std::fs::read_dir(&notebooks_dir) {
            for entry in entries.flatten().take(5) {
                let name = entry.file_name().to_string_lossy().to_string();
                sources.push(format!("Pinned: {name}"));
            }
        }
    }

    sources
}

async fn create_notebooklm_notebook(
    date: &str,
    _sources: &[String],
) -> Result<String> {
    // Try nlm CLI first
    let output = tokio::process::Command::new("nlm")
        .args([
            "notebook",
            "create",
            "--title",
            &format!("Morning Brief — {date}"),
        ])
        .stdout(std::process::Stdio::piped())
        .stderr(std::process::Stdio::piped())
        .stdin(std::process::Stdio::null())
        .output()
        .await;

    match output {
        Ok(o) if o.status.success() => {
            let stdout = String::from_utf8_lossy(&o.stdout).trim().to_string();
            // Try to extract notebook ID from output
            if let Some(id) = stdout.lines().find(|l| l.len() > 20 && l.len() < 60) {
                Ok(id.trim().to_string())
            } else {
                Ok(uuid::Uuid::new_v4().to_string())
            }
        }
        _ => {
            // nlm not available — generate a placeholder ID
            anyhow::bail!("nlm CLI not available or failed")
        }
    }
}

// ─── Daily activity gathering (for reflections) ──────────

fn gather_daily_activity(vault_path: &Path, today_str: &str) -> String {
    let mut parts = Vec::new();

    // Completed jobs today
    let completed_dir = vault_path.join("_jobs").join("completed");
    if completed_dir.exists() {
        let mut count = 0;
        let mut job_names = Vec::new();
        if let Ok(entries) = std::fs::read_dir(&completed_dir) {
            for entry in entries.flatten() {
                if let Ok(meta) = entry.metadata() {
                    if let Ok(modified) = meta.modified() {
                        let dt: chrono::DateTime<chrono::Utc> = modified.into();
                        if dt.format("%Y-%m-%d").to_string() == today_str {
                            count += 1;
                            if job_names.len() < 5 {
                                job_names.push(
                                    entry.file_name().to_string_lossy().to_string(),
                                );
                            }
                        }
                    }
                }
            }
        }
        parts.push(format!("- Completed jobs: {count}"));
        if !job_names.is_empty() {
            parts.push(format!("  - Examples: {}", job_names.join(", ")));
        }
    }

    // Pending jobs
    let pending = vault_path
        .join("_jobs")
        .join("pending")
        .read_dir()
        .map(|e| e.count())
        .unwrap_or(0);
    parts.push(format!("- Pending jobs: {pending}"));

    // Daemon status
    let status_path = vault_path.join("DAEMON-STATUS.md");
    if let Ok(content) = std::fs::read_to_string(&status_path) {
        if let Some(runs_line) = content.lines().find(|l| l.contains("total_runs:")) {
            parts.push(format!("- {}", runs_line.trim()));
        }
        if let Some(errors_line) = content.lines().find(|l| l.contains("total_errors:")) {
            parts.push(format!("- {}", errors_line.trim()));
        }
    }

    // News highlights
    let news = read_news_summary(vault_path);
    if !news.is_empty() {
        let news_lines: Vec<&str> = news.lines().take(5).collect();
        parts.push(format!("- Top news:\n{}", news_lines.join("\n")));
    }

    if parts.is_empty() {
        "No significant activity data available.".to_string()
    } else {
        parts.join("\n")
    }
}

fn read_today_touchpoints(vault_path: &Path, today_str: &str) -> String {
    let log_path = vault_path.join("_system").join("TOUCHPOINT-LOG.md");
    if let Ok(content) = std::fs::read_to_string(&log_path) {
        let today_entries: Vec<&str> = content
            .lines()
            .filter(|l| l.contains(today_str))
            .collect();
        if today_entries.is_empty() {
            "No touchpoint activity today.".to_string()
        } else {
            format!(
                "{} touchpoint events today:\n{}",
                today_entries.len(),
                today_entries.iter().take(15).copied().collect::<Vec<_>>().join("\n")
            )
        }
    } else {
        "Touchpoint log not available.".to_string()
    }
}
