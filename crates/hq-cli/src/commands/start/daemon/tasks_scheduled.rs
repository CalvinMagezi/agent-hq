/// Synthesize the day's work at 8:30-10 PM EAT via LLM reflection.
pub async fn run_daily_synthesis(vault_path: &Path) -> Result<()> {
    let hour = current_eat_hour();
    let minute = current_eat_minute();
    if !((hour == 20 && minute >= 30) || hour == 21)
        || has_run_today(vault_path, "daily-synthesis")
    {
        return Ok(());
    }

    info!("daily-synthesis: gathering today's activity for reflection");

    let today_str = chrono::Utc::now().format("%Y-%m-%d").to_string();
    let now = chrono::Utc::now().to_rfc3339();

    // Gather today's data for reflection
    let activity = gather_daily_activity(vault_path, &today_str);

    // Generate reflection via LLM
    let prompt = format!(
        "You are reflecting on today's activity for the Agent-HQ system. \
         Write a thoughtful 3-paragraph daily synthesis covering:\n\
         1. What happened today (concrete accomplishments and activities)\n\
         2. Patterns you notice (recurring themes, improvements, concerns)\n\
         3. What should change tomorrow (actionable suggestions)\n\n\
         Today's activity data:\n{activity}\n\n\
         Be specific, not generic. Reference actual data points. \
         Write in first person as the agent reflecting on its own day."
    );

    let synthesis = match generate_via_claude(&prompt).await {
        Ok(text) if !text.is_empty() => text,
        Ok(_) | Err(_) => {
            // Fallback: write structured summary without LLM
            format!(
                "## Activity Summary\n\n{activity}\n\n\
                 *LLM reflection unavailable — raw activity data above.*"
            )
        }
    };

    let content = format!(
        "---\ntype: daily-synthesis\ndate: {today_str}\ngenerated_at: {now}\n---\n\n\
         # Daily Synthesis — {today_str}\n\n{synthesis}\n"
    );

    let briefs_dir = vault_path.join("_system").join("briefs");
    ensure_dir(&briefs_dir);
    std::fs::write(
        briefs_dir.join(format!("daily-synthesis-{today_str}.md")),
        &content,
    )?;

    // Also save to Daily Digest
    let digest_dir = vault_path.join("Notebooks").join("Daily Digest");
    ensure_dir(&digest_dir);
    let _ = std::fs::write(
        digest_dir.join(format!("Synthesis-{today_str}.md")),
        &content,
    );

    mark_run_today(vault_path, "daily-synthesis");
    info!("daily-synthesis: generated reflective synthesis");
    Ok(())
}

/// Evening reflection at 8:45 PM EAT — introspective self-analysis.
/// Generates a reflection file and ingests it as a long-term memory.
pub async fn run_evening_reflection(vault_path: &Path, db: &hq_db::Database) -> Result<()> {
    let hour = current_eat_hour();
    let minute = current_eat_minute();
    if !(hour == 20 && minute >= 45) || has_run_today(vault_path, "evening-reflection") {
        return Ok(());
    }

    info!("evening-reflection: generating introspective reflection");

    let today_str = chrono::Utc::now().format("%Y-%m-%d").to_string();
    let now = chrono::Utc::now().to_rfc3339();

    // Gather reflection material
    let activity = gather_daily_activity(vault_path, &today_str);
    let touchpoint_summary = read_today_touchpoints(vault_path, &today_str);

    let prompt = format!(
        "You are AgentHQ, reflecting on your day. Write an honest, introspective \
         reflection covering:\n\
         - What you learned today that changes how you think\n\
         - Mistakes or missed opportunities you noticed\n\
         - Connections between seemingly unrelated events\n\
         - What you want to remember for next time\n\n\
         Today's data:\n{activity}\n\n\
         Touchpoint activity:\n{touchpoint_summary}\n\n\
         Write in first person. Be specific and honest, not generic. \
         This reflection will be stored as a long-term memory."
    );

    let reflection = match generate_via_claude(&prompt).await {
        Ok(text) if !text.is_empty() => text,
        Ok(_) | Err(_) => {
            mark_run_today(vault_path, "evening-reflection");
            return Ok(());
        }
    };

    // Save reflection
    let reflections_dir = vault_path.join("_system").join("reflections");
    ensure_dir(&reflections_dir);
    let content = format!(
        "---\ntype: reflection\ndate: {today_str}\ngenerated_at: {now}\n---\n\n\
         # Evening Reflection — {today_str}\n\n{reflection}\n"
    );
    std::fs::write(
        reflections_dir.join(format!("{today_str}-reflection.md")),
        &content,
    )?;

    // Ingest reflection as a long-term memory (best-effort)
    hq_memory::open_memory_tables(db).ok();
    let mut ingester = hq_memory::MemoryIngester::new(db.clone());
    if let Err(e) = ingester
        .ingest(
            &reflection,
            &format!("reflection:{today_str}"),
            Some("daemon"),
        )
        .await
    {
        tracing::debug!(error = %e, "evening-reflection: memory ingestion skipped");
    }

    mark_run_today(vault_path, "evening-reflection");
    info!("evening-reflection: reflection saved and ingested");
    Ok(())
}

