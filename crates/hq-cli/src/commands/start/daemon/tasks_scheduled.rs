/// Generate end-of-day summary at 8 PM EAT.
pub async fn run_daily_brief(vault_path: &Path) -> Result<()> {
    let hour = current_eat_hour();
    if hour != 20 || has_run_today(vault_path, "daily-brief") {
        return Ok(());
    }

    let now = chrono::Utc::now().to_rfc3339();
    let today_str = chrono::Utc::now().format("%Y-%m-%d").to_string();

    let completed_dir = vault_path.join("_jobs").join("completed");
    let completed_count = if completed_dir.exists() {
        std::fs::read_dir(&completed_dir)
            .map(|entries| {
                entries
                    .flatten()
                    .filter(|e| {
                        e.metadata()
                            .ok()
                            .and_then(|m| m.modified().ok())
                            .map(|t| {
                                let dt: chrono::DateTime<chrono::Utc> = t.into();
                                dt.format("%Y-%m-%d").to_string() == today_str
                            })
                            .unwrap_or(false)
                    })
                    .count()
            })
            .unwrap_or(0)
    } else {
        0
    };

    let content = format!(
        "---\ntype: daily-brief\ndate: {today_str}\ngenerated_at: {now}\n---\n\n\
         # Daily Brief — {today_str}\n\n\
         ## Summary\n\n\
         - **Completed jobs**: {completed_count}\n\
         - **Generated at**: {now}\n\n\
         ## Activity\n\nEnd-of-day summary generated by daemon.\n"
    );
    let briefs_dir = vault_path.join("_system").join("briefs");
    ensure_dir(&briefs_dir);
    std::fs::write(
        briefs_dir.join(format!("daily-brief-{today_str}.md")),
        content,
    )?;
    mark_run_today(vault_path, "daily-brief");
    info!("daily-brief: generated end-of-day summary");
    Ok(())
}

/// Generate morning brief script + audio at 6 AM EAT.
/// Calls LLM for script generation, then Kokoro TTS for audio.
pub async fn run_morning_brief_audio(vault_path: &Path) -> Result<()> {
    let hour = current_eat_hour();
    if hour != 6 || has_run_today(vault_path, "morning-brief-audio") {
        return Ok(());
    }

    // Always run (no env var gate — this is a core feature)
    info!("morning-brief-audio: generating morning brief script + audio");

    let today_str = chrono::Utc::now().format("%Y-%m-%d").to_string();
    let briefs_dir = vault_path.join("_system").join("briefs");
    ensure_dir(&briefs_dir);

    // Gather context for the LLM
    let context = gather_morning_context(vault_path);

    // Generate script via LLM (claude -p with a crafted prompt)
    // Read identity from SOUL.md (gitignored vault), not hardcoded in source
    let soul_path = vault_path.join("_system").join("SOUL.md");
    let identity = std::fs::read_to_string(&soul_path)
        .ok()
        .and_then(|c| {
            let t = c.trim();
            if t.starts_with("---") {
                t[3..].find("---").map(|end| t[3 + end + 3..].trim().to_string())
            } else {
                Some(t.to_string())
            }
        })
        .unwrap_or_else(|| "a tech professional".to_string());
    let identity_short: String = identity.chars().take(200).collect();

    let prompt = format!(
        "You are writing a 2-minute morning briefing script for the vault owner. \
         Context about them: {identity_short}\n\n\
         Write it as a natural two-speaker dialogue using [S1] and [S2] tags. Cover:\n\n\
         {context}\n\n\
         Make it conversational, insightful, and actionable — not a generic news recap. \
         Prioritize Africa-relevant stories, AI/developer tooling news, and active projects. \
         Output ONLY the dialogue, no preamble or markdown formatting."
    );

    let script = match generate_via_claude(&prompt).await {
        Ok(text) if !text.is_empty() => text,
        Ok(_) => {
            tracing::warn!("morning-brief-audio: LLM returned empty script, using fallback");
            format!(
                "[S1]: Good morning! Let's check in on what's happening today.\n\
                 [S2]: {context}\n\
                 [S1]: That's the overview. Let's make it a productive day."
            )
        }
        Err(e) => {
            tracing::warn!(error = %e, "morning-brief-audio: LLM script generation failed");
            mark_run_today(vault_path, "morning-brief-audio");
            return Ok(());
        }
    };

    // Save script
    let script_path = briefs_dir.join(format!("morning-script-{today_str}.md"));
    let script_md = format!(
        "---\nnoteType: note\ntags:\n  - daily-brief\n  - script\ndate: {today_str}\n---\n\n\
         # Morning Brief Script — {today_str}\n\n{script}"
    );
    std::fs::write(&script_path, &script_md)?;
    info!("morning-brief-audio: saved script to {:?}", script_path);

    // Save to Daily Digest too
    let digest_dir = vault_path.join("Notebooks").join("Daily Digest");
    ensure_dir(&digest_dir);
    let _ = std::fs::write(
        digest_dir.join(format!("Brief-{today_str}-script.md")),
        &script_md,
    );

    // Generate audio via Kokoro TTS (best-effort)
    let audio_path = briefs_dir.join(format!("morning-audio-{today_str}.wav"));
    match generate_tts(&script, audio_path.to_str().unwrap_or_default()).await {
        Ok(true) => info!("morning-brief-audio: generated audio at {:?}", audio_path),
        Ok(false) => tracing::debug!("morning-brief-audio: TTS not available, skipping audio"),
        Err(e) => tracing::warn!(error = %e, "morning-brief-audio: TTS failed"),
    }

    mark_run_today(vault_path, "morning-brief-audio");
    Ok(())
}

/// Create NotebookLM deep-dive at 6:30 AM EAT.
pub async fn run_morning_brief_notebooklm(vault_path: &Path) -> Result<()> {
    let hour = current_eat_hour();
    let minute = current_eat_minute();
    if !(hour == 6 && minute >= 30) || has_run_today(vault_path, "morning-brief-notebooklm") {
        return Ok(());
    }

    info!("morning-brief-notebooklm: creating NotebookLM deep-dive");

    let today_str = chrono::Utc::now().format("%Y-%m-%d").to_string();
    let briefs_dir = vault_path.join("_system").join("briefs");
    ensure_dir(&briefs_dir);

    // Gather sources for NotebookLM
    let sources = gather_notebooklm_sources(vault_path);

    // Use nlm CLI to create notebook (best-effort)
    let notebook_id = match create_notebooklm_notebook(&today_str, &sources).await {
        Ok(id) => id,
        Err(e) => {
            tracing::warn!(error = %e, "morning-brief-notebooklm: failed to create notebook");
            mark_run_today(vault_path, "morning-brief-notebooklm");
            return Ok(());
        }
    };

    // Save reference note
    let focus_prompt = "This is a personalized daily briefing for the vault owner. \
        Frame everything through a practical lens for a tech leader building AI systems. \
        Cover his active project context from the pinned notes, any engineering wins from \
        recent commits, and the most relevant tech and Africa news. Make it conversational, \
        insightful, and actionable — not a generic news recap. Prioritize Africa-relevant \
        stories and AI/developer tooling news.";

    let source_list: String = sources
        .iter()
        .map(|s| format!("- {s}"))
        .collect::<Vec<_>>()
        .join("\n");

    let note_content = format!(
        "---\ntitle: NotebookLM Brief — {today_str}\ndate: {today_str}T00:00:00.000Z\n\
         tags:\n  - daily-brief\n  - notebooklm\nnotebook_id: {notebook_id}\n\
         audio_format: deep_dive\n---\n\n\
         # NotebookLM Brief — {today_str}\n\
         **Notebook**: [Open in NotebookLM](https://notebooklm.google.com/notebook/{notebook_id})\n\
         ## Sources Used\n{source_list}\n\
         ## Focus Prompt\n> {focus_prompt}\n"
    );

    let digest_dir = vault_path.join("Notebooks").join("Daily Digest");
    ensure_dir(&digest_dir);
    let _ = std::fs::write(
        digest_dir.join(format!("Brief-{today_str}-notebooklm.md")),
        &note_content,
    );

    mark_run_today(vault_path, "morning-brief-notebooklm");
    info!(notebook_id, "morning-brief-notebooklm: created notebook");
    Ok(())
}

